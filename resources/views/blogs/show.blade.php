                                    <p>آب، بخار و یا هوا. بعد از عبور از شبکه های انتقال به وسائل توزیع گرما و سرما رسیده تهویه مطبوع محیط را فراهم می آوردند وسائل توزیع سرما و گرما در اینجا فن کویل و هوا ساز می باشد. از فن کویل برای گرمایش در زمستان و هم برای سرمایش در تابستان مورد استفاده می شود. آب سرد و گرمی که در دستگاههای مولد انرژی (دیگ ـچلیر) تولید می شود توسط پمپ و شبکه لوله کشی به فن کویل رسیده و پس از عبور از کویل های پره دار به وسیله وزش اجباری هوا روی کویل ها ، سرما و گرما را به محیط می دهد.</p>
