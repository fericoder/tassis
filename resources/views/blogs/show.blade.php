                                    <h4>چیلر در موتور خانه</h4>
                                    <p class="mb-30">چیلرها معمولاً در دو نوع جذبی و تراکمی ساخته می شوند بدلیل مصرف برق زیاد توسط چیلرهای تراکمی (کمپرسوری) امروزه چیلرهای جذبی از استقبال خوبی در میان مهندسین مشاور و صاحبان ساختمانهای مسکونی و اداری برخوردار شده اند ، این نوع چیلرها بجای انرژی برق از انرژی حرارتی برای تولید سرما استفاده مینمایند و دارای قطعات متحرک کمتری نسبت به انواع کمپرسوری هستند و با توجه به ماهیت چرخشی کار پمپهای مورد استفاده در آنها میزان خرابی و هزینه های مربوط به تعمیرات آنها کمتر از انواع تراکمی می باشد ، همچنین صدای آنها بسیار کمتر از انواع تراکمی بوده و تقریباً بدون لرزش هستند ، با در نظر گرفتن هزینه های جنبی از جمله هزینه مربوط به خرید امتیاز برق و دیماند مربوطه و همچنین هزینه های جاری چیلر تراکمی ، چیلرهای جذبی از نظر اقتصادی نیز دارای مزیت قابل توجهی هستند.</p>
