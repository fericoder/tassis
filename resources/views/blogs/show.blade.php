                                    <h2>موتورخانه ها</h2>
                                    <p>موتورخانه بخشی از ساختمان است که به عنوان قلب سیستم تاسیساتی خانه شناخته می شود. این فضا به عنوان کنترل کننده سیستم ها گرمایشی و سرمایشی و وسایل و تجهیزات الکتریکی ساختمان مورد استفاده قرار می گیرد. طراحی این فضا به علت تجهیزات و وسایلی که در آن قرار می گیرد بسیار مهم است ، به همین دلیل تصمیم گرفتیم تا در این مطلب به معرفی فضای موتورخانه بپردازیم:</p>
                                    <p class="mb-30">در ساختمان ها تمامی تجهیزات تاسیساتی سیستم به یک فضا انتقال داده می شود که کنترل و تعمیر آن ها را آسان تر می کند. به محل قرار گرفتن این تجهیزات، موتورخانه می گویند.</p>
                                    <h4>محل قرار گیری موتورخانه ها</h4>
                                    <p>این اتاق معمولا به دستگاه های مکانیکی و کنترل کننده های الکتریکی ، اختصاص داده می شود. معمولا محل قرار گیری موتورخانه ها در زیر زمین خانه است و در محل ها و ساختمان های بزرگ تر معمولا این فضا در خارج از ساختمان قرار دارد و آن هم به دلیل سنگینی تاسیسات و سر و صدای زیادی است که تولید می کنند.</p>
                                    <h4>اجزای موجود در موتورخانه</h4>
                                    <p>معمولا هدف تجهیزاتی که در موتورخانه هستند ، تهویه مطبوع و گرمایش ساختمان است. در سیستم های تهویه مطبوع و گرمایش ، روش های متتعددی به کار گرفته می شود و بنا بر هر کدام از این روش ها تجهیزات متفاوتی مورد استفاده قرار می گیرد.</p>
                                    <h4>دیگ ها در موتور خانه </h4>
                                    <p>از معمول ترین مولد های گرمایی،دیگ های حرارت مرکزی می باشند . در دیگ ها انرژی حرارتی از سوخت (که توسط مشعل تولید حرارت می نماید ) گرفته شده و به آن داده می شود . بسته به نوع مصرف، آب می تواند در دیگ ها به آب گرم (87 درجه سیلسیوس)، آب داغ (200 درجه سیلسیوس با افزایش فشار آب) و یا بخار تبدیل شود لذا دیگ ها به سه نوع دیگ آبگرم ، آب داغ و دیگ بخار تقسیم می شوند .در دیگهای آب گرم انرژی حرارتی تولید شده توسط مشعل، آب گرم یا داغ را تولید می نماید و سعی می شود که از تولید بخار جلوگیری به عمل آید زیرا در این دیگ ها کنترل و سوپاپی جهت بخار ایجاد شده وجود ندارد و در صورت تولید بخار به دیگ آسیب می رسد. دیگ ها آب گرم از نظر جنس و ساخت به دو نوع : چدنی و فولادی تقسیم می شوند.</p>
